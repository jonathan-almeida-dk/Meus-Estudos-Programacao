# Problema:
# Mostrar os números de 1 a 10.
# Antes do código:
# • Qual o objetivo?
# • Quais dados preciso?
# • Quais passos executar?
for n in range(1,11):
    print(n)
# explicação: 'n' é cada número dentro da range entre 1,10(o 11 não conta)
# logo, quando se der print em 'n', ele percorre a lista de numeros dentro da range,
# mostrando um em seguida do outro.
