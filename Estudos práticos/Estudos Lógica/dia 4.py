# Descobrir o maior número de uma lista.

lista = [5,10,20,45,20,1]                     # lista criada
maior = max(lista)                            # variável 'maior' recebe o maior número da lista
print(lista)                                  # printa a lista
print(f'O maior número da lista é o {maior}') # printa o maior número da lista

